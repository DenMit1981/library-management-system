CREATE TABLE IF NOT EXISTS books (
    id BIGINT PRIMARY KEY NOT NULL,
    isbn VARCHAR(500) NOT NULL,
    title VARCHAR(500) NOT NULL,
    author VARCHAR(500) NOT NULL,
    borrower_id BIGINT,
    FOREIGN KEY (borrower_id) REFERENCES users(id)
);

